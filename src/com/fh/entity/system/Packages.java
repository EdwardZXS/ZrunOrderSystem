package com.fh.entity.system;

public class Packages {

	private int PACKAGES_ID ;
	private int PRODUCTID;
	private String PACKAGE_NAME;
	private double PACKAGE_PRICE;
	private String BAK30;
	
	
	public int getPACKAGES_ID() {
		return PACKAGES_ID;
	}
	public void setPACKAGES_ID(int pACKAGES_ID) {
		PACKAGES_ID = pACKAGES_ID;
	}
	public int getPRODUCTID() {
		return PRODUCTID;
	}
	public void setPRODUCTID(int pRODUCTID) {
		PRODUCTID = pRODUCTID;
	}
	public String getPACKAGE_NAME() {
		return PACKAGE_NAME;
	}
	public void setPACKAGE_NAME(String pACKAGE_NAME) {
		PACKAGE_NAME = pACKAGE_NAME;
	}
	public double getPACKAGE_PRICE() {
		return PACKAGE_PRICE;
	}
	public void setPACKAGE_PRICE(double pACKAGE_PRICE) {
		PACKAGE_PRICE = pACKAGE_PRICE;
	}
	public String getBAK30() {
		return BAK30;
	}
	public void setBAK30(String bAK30) {
		BAK30 = bAK30;
	}
	
	

}
